
public class Item {

    private int luckyNumver;
    private int age;

    public Item (int luckyNumver, int age) {
        this.luckyNumver = luckyNumver;
        this.age = age;
    }

    public int getLuckyNumver() {
        return luckyNumver;
    }

    public int getAge() {
        return age;
    }
}
